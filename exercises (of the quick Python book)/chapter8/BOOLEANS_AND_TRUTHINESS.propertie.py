'''
Decide whether the following statements are true or false:
1, 0, -1, [0], 1 and 0, 1 > 0 or []
'''
print(bool(1))
# output: True
print(bool(0))
# output: False
print(bool(-1))
# output: True
print(bool([0]))
# output: True
print(bool(1 and 0))
# output: False
print(bool(1 > 0 or []))
# output: True
